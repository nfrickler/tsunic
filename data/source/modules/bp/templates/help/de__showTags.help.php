<h2>Tags</h2>
<p>
    Ein Tag legt Namen und Typ einer Information fest, die zu einem Objekt
    hinzugefügt werden soll (z.B. "Vorname" (Zeichenkette),
    "Geburtsdatum" (Datum), "Alter" (Ganzzahl)).
</p>
<p>
    Auf dieser Seite findet man eine Übersicht aller vorhandenen Tags.
    Diese können umbenannt, erweitert und gelöscht, sowie neue hinzugefügt
    werden.
</p>
