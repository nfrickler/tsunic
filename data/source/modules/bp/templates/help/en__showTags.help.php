<h2>Tags</h2>
<p>
    A tag defines name and type of an information that can be added to
    an object (e.g. "Firstname" (String), "Date of birth" (Date),
    "Age" (Integer)).
</p>
<p>
    This page shows a summary of all available Tags. You can rename, edit or delete them or add new ones.
</p>
