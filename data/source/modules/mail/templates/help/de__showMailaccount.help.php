<h2>Mailaccount anzeigen</h2>
<p>
    Diese Seite gibt dir einen Überblick über einen Mailaccount.
    Du kannst den Mailaccount bearbeiten, löschen, einen SMTP-Server
    verknüpfen und dessen Serverboxen verwalten.
</p>
<h3>Serverboxen verwalten</h3>
<p>
    Serverboxen sind die Mailboxen auf dem externen IMAP/POP3-Server und sollten
    automatisch gefunden und hier aufgelistet werden.<br/>
    Um E-Mails aus einer dieser Serverboxen auf TSunic umzuleiten bzw. eine
    Serverbox mit einer lokalen Mailbox zu verknüpfen, aktiviere diese
    Serverbox, indem du das Häkchen setzt.<br/>
    Du kannst auswählen, in welche lokale Mailbox die E-Mails geladen werden
    sollen. Dies kannst du ändern, indem du die Serverbox bearbeitest.
</p>
