<h2>Das Mailsystem</h2>
<p>
    Dies ist das Mailsystem von TSunic. Dieses ermöglicht dir, E-Mails von
    allen deinen verschiedenen Mailaccounts (via IMAP/POP3) zu laden und zu
    hier zentral zu verwalten.<br/>
    Außerdem kannst du auch E-Mails über diese Accounts versenden (via SMTP).
</p>
<h3>Mails empfangen</h3>
<p>
    Als erstes musst du deinen Mailaccount unter "Mailaccounts" mit TSunic
    verlinken. Trage dort deine E-Mail-Logindaten ein und TSunic wird versuchen,
    alle restlichen Verbindungsdaten für den Mailaccount automatisch
    herauszufinden.<br/>
    Anschließend kannst du die Mailboxen auf dem Server (=Serverboxen) mit
    lokalen Mailboxen auf TSunic verknüpfen. Mails in diesen Serverboxen werden
    daraufhin jeweils in die lokale Mailbox geladen und regelmäßig auf neue
    E-Mails durchsucht.
</p>
<h3>Mails senden</h3>
<p>
    Um eine Mail über einen bestimmten Account zu senden, verknüpfe zuerst den
    entsprechenden SMTP-Server mit TSunic. Danach kannst du beim Verfassen
    von neuen Mails diesen SMTP-Server auswählen und die Mail abschicken.
</p>
<h3>Lokale Mail-Funktion</h3>
<p>
    Falls der Administrator dies zulässt, kannst du auch E-Mails über den
    lokalen Mailserver versenden. Dazu muss kein eigener SMTP-Server
    verknüpft werden.
</p>
