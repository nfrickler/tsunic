<h2>Show mailaccount</h2>
<p>
    This page shows you the most important details of an mailaccount.
    You can edit and delete the mailaccount, link a SMTP server and
    administrate its serverboxes.
</p>
<h3>Serverboxes</h3>
<p>
    Serverboxes are the mailboxes on the external IMAP/POP3 server and
    should be detected by TSunic automatically.<br/>
    To load mails from a serverbox into a locale mailbox, activate the
    serverbox by checking the checkbox in front.<br/>
    You can choose the destination mailbox of mails by editing the
    serverbox.
</p>
