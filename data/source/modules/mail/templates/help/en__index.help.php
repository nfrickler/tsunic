<h2>The mail system</h2>
<p>
    This is the mail system of TSunic. It enables you to recieve and
    administrate e-mail of different mailaccounts (via IMAP/POP3).</br>
    Moreover, you can send e-mails using this accounts (via SMTP).
</p>
<h3>Receive mails</h3>
<p>
    First you have to link you mailaccount with TSunic. Enter you e-mail
    logindata. TSunic will try to find the rest of the connection details
    automatically.<br/>
    Afterwards you can link the mailboxes on the server (= serverboxes) with
    local mailboxes of TSunic to load all mails from the serverboxes to
    the local mailboxes. New mails will be downloaded automatically into
    these mailboxes as well.
</p>
<h3>Send mails</h3>
<p>
    To send mails using a certain mailaccount, link the SMTP-server with TSunic
    so you can send e-mails from it.
</p>
<h3>Local mail function</h3>
<p>
    If the administrator allows it, you can additionally send e-mails using
    the local mail server. You don't have to add an own SMTP-server then.
</p>
