<h3>Das Formular</h3>
<dl>
    <dt>Benutzername</dt>
    <dd>
	Der Benutzername ist frei wählbar und muss auf dem gesamten
	System eindeutig sein.
	Bitte beachte, dass dein Benutzername für alle sichtbar ist
	und zur eigenen Sicherheit möglichst keine persönlichen
	Daten (z.B. echter Name, Geburtstag, Alter, etc.) enthalten
	sollte.<br />
	Der Benutzername muss zwischen 3 und 30 Zeichen lang sein.
    </dd>
    <dt>E-Mail Adresse</dt>
    <dd>
	Über diese E-Mail Adresse sollte dich TSunic bzw. sein
	Administrator erreichen können. Diese E-Mail Adresse ist
	nur für Administratoren sichtbar.
    </dd>
    <dt>Passwort</dt>
    <dd>
	Gebe hier ein Passwort für deinen Account ein. Dieses Passwort
	benötigst du beim Login und bei schwerwiegenden Aktionen (z.B.
	beim Löschen des Accounts).<br/>
	Wähle möglichst ein sicheres Passwort:
	<ul>
	    <li>Mix aus Groß-/Kleinbuchstaben, Zahlen und
		Sonderzeichen</li>
	    <li>Keine Sachen, die mit einem selber zu tun haben
		(Name, Geburtstag, Geburtstag von Verwandten/Freunden,
		Haustier, etc.)</li>
	    <li>Nichts, was im Wörterbuch steht</li>
	    <li>Mindestens 7 Stellen lang</li>
	    <li>Kein Passwort, das du bereits woanders verwendest</li>
	</ul>
	Halte dein Passwort geheim! Niemand braucht dein Passwort (auch
	keine Administratoren oder ähnliches)!<br/>
	<strong>Achtung:</strong> TSunic verschlüsselt viele Daten mit
	deinem Passwort. Vergisst du dieses, können diese Daten nicht
	wiederhergestellt werden!
    </dd>
    <dt>Passwort wiederholen</dt>
    <dd>
	Wiederhole hier nochmal dein Passwort, um sicherzustellen, dass
	du dich nicht vertippt hast.
    </dd>
</dl>
