<h2>Show directory</h2>
<p>
	TSunic offers every user a little filesystem to upload and save
	files.
	To improve administration of your files you can create
	additional subdirectories.
</p>
<p>
	This page shows the content of this directory with all its
	subdirectories and files.
</p>
<p>
	You can create a new directory and edit the current one. Moreover, you
	can upload a new file (links on the top).<br/>
	Furthermore, all subdirectories and files can be deleted. Files can be
	edited as well (links in the action column).<br/>
	Clicking on a directory opens it, a click on a file starts the
	download of this file.
</p>
