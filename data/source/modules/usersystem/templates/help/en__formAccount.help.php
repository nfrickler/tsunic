<h3>The formular</h3>
<dl>
    <dt>Username</dt>
    <dd>
	Choose a username you like. This username has to be unique
	on this system and have to consist of 3 to 30 letters.
	Please note, that your username is visible to the public. For
	your own safety you may not use any personal information
	within your username (e.g. real name, age, date of birth etc.)
    </dd>
    <dt>E-mail address</dt>
    <dd>
	This is the e-mail address, TSunic and it's administrators
	should be able to contact you.
	The e-mail address is visible to administrators of this system
	only.
    </dd>
    <dt>Password</dt>
    <dd>
	Enter a password for you account. You will need this password
	for logging in and for some actions (e.g. deleting your
	account).<br/>
	You should really use a secure password:
	<ul>
	    <li>Mix of Upper-/Lowercase letters, numbers and special
		characters</li>
	    <li>The password should not contain any personal data
		(name, date of birth, friends, family, name of
		pet etc.)</li>
	    <li>Nothing from a dictionary</li>
	    <li>Seven characters at least</li>
	    <li>No password, you are already using elsewhere.</li>
	</ul>
	Keep you password secret! Noone needs to know it (not even the
	administrator of this system)!<br/>
	<strong>Attention:</strong> TSunic is encrypting data using your
	password. If you lost this password, these data cannot be
	recreated!
    </dd>
    <dt>Passwort wiederholen</dt>
    <dd>
	Wiederhole hier nochmal dein Passwort, um sicherzustellen, dass
	du dich nicht vertippt hast.
    </dd>
</dl>
