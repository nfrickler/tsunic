<h2>Verzeichnis anzeigen</h2>
<p>
    Bei TSunic hat jeder Nutzer ein eigenes kleines Dateisystem.
    In diesem Dateisystem kannst du Dateien hochladen und speichern.
    Um deine Dateien besser zu verwalten kannst du auch weitere Unterordner
    anlegen, in denen du weitere Dateien speichern kannst.
</p>
<p>
    Diese Seite zeigt den Inhalt eines Verzeichnis im Dateisystem.
    Aufgelistet werden alle Unterordner und Dateien im Verzeichnis.
</p>
<p>
    Du kannst ein neues Verzeichnis anlegen, das aktuelle Verzeichnis
    bearbeiten oder eine neue Datei hochladen (Links oben).<br/>
    Außerdem können alle Unterordner und Dateien im Verzeichnis
    gelöscht, Dateien zusätzlich bearbeitet werden
    (Links in der Spalte Aktion).<br/>
    Mit einem Klick auf einen Ordner, wird dieser geöffnet.<br/>
    Ein Klick auf eine Datei startet den Download dieser Datei.
</p>
